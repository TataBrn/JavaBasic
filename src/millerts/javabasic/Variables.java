package millerts.javabasic;

public class Variables {
    public static void main(String[] args) {
        //Переменная типа с плавающей точкой с наибольшим диапазоном чисел с произвольным значением
        double d = 78.55d;

        //Переменная целочисленного типа с наименьшим диапазоном чисел с наибольшим возможным значением
        byte b = 127;
    }
}
